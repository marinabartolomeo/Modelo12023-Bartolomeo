    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed
        try{
           // conectar a la base de datos
           Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/agenda", "root", "");
           // Enviando la sentencia sql
           PreparedStatement sq = conexion.prepareStatement("insert into personas values(?,?,?,?)");
           //Especifico los campos
           sq.setString(1, "0");
           sq.setString(2, txt_nombre.getText().trim());
           sq.setString(3, txt_apellido.getText().trim());
           sq.setString(4, txt_telefono.getText().trim());
           //Todo lo pedido que lo ejecute y cierre la base
           sq.executeUpdate();
           
          //limpiar los campos
           txt_nombre.setText("");
           txt_apellido.setText("");
           txt_telefono.setText("");
           // informar la carga
           label_status.setText("Registro Exitoso");
           
       }catch(SQLException e){
       
       }
    }//GEN-LAST:event_CrearActionPerformed

