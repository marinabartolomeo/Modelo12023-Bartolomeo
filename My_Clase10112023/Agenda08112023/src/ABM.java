    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
try {
  // conectar a la base de datos
           Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/agenda", "root", "");
           // Enviando la sentencia sql
           PreparedStatement sq = conexion.prepareStatement("select * from personas where IdDato = ?");
           //Especifico los campos
           sq.setString (1, txt_buscar.getText());
           ResultSet rs = sq.executeQuery();
    // poner lo que buscamos en el campo 
    
            if (rs.next()) {
                txt_nombre.setText (rs.getString("Nombre"));
                txt_apellido.setText (rs.getString("Apellido"));
                txt_telefono.setText (rs.getString("Telefono"));
            }else {
                JOptionPane.showMessageDialog(null,"El dato no se encontro");
            }    
}catch (Exception e){
   
}              
        
    }//GEN-LAST:event_BuscarActionPerformed

