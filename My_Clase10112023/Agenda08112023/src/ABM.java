    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        try{
           // conectar a la base de datos 
           Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/agenda", "root", "");
           // Enviando la sentencia sql
          
           String ID = txt_buscar.getText().trim();
           PreparedStatement sq = conexion.prepareStatement("update personas set Nombre =?,Apellido=?,Telefono=? where IdDato="+ ID);
           //Especifico los campos 
           sq.setString(1, txt_nombre.getText().trim());
           sq.setString(2, txt_apellido.getText().trim());
           sq.setString(3, txt_telefono.getText().trim());
           //Todo lo pedido que lo ejecute y cierre la base
           sq.executeUpdate();
           
           
           //limpiar los campos
           txt_nombre.setText("");
           txt_apellido.setText("");
           txt_telefono.setText("");
           txt_buscar.setText("");
           // informar la carga
           label_status.setText("Registro modificado");
           
       }catch(SQLException e){
       
       }
    }//GEN-LAST:event_ModificarActionPerformed

